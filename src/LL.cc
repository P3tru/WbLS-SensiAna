double EvalNLL(double Nobs, double Npred){
  double L;
  if(Nobs>0 && Npred>0)
	L=Nobs*TMath::Log(Nobs/Npred) + Npred-Nobs;
  else
	L=Npred;
  L = -L;
  return L;
}
